    public void cargarTabla(){
        
        ControladorCategoria cont = new ControladorCategoria();
        //categorias= null;
        //categorias.addColumn("Categoria");
        categorias = new DefaultTableModel();
        categorias.addColumn("Categoria");
        int a =categorias.getRowCount();
            for(int i=0; i<a; i++){
                categorias.removeRow(0);
            }
            
        this.jTable1.setModel(new DefaultTableModel()); 
        this.jTable1.setModel(categorias);
        List<DataCompuesta> DataListaComp = new LinkedList();        
        DataListaComp = cont.ListarCompuestas();
        //Iterator it = DataListaComp.iterator();
        System.out.print(DataListaComp.size());
        for(int i=0;i<DataListaComp.size();i++){
            if(!"Raiz".equals(DataListaComp.get(i).GetNombre()))
            categorias.addRow(new Object[]{DataListaComp.get(i).GetNombre()});
        
        }
        
        
    
    }
    
    
   
    
    
    
    
